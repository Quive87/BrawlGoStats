def calculate_trophy_change(mode, trophies, rank=None, result=None):
    """
    Calculates the expected trophy change for a brawler based on mode and placement.
    """
    m = mode.lower().replace(" ", "")
    
    if "soloshowdown" in m:
        bracket = find_bracket(trophies, SOLO_SHOWDOWN)
        if bracket and rank and 1 <= rank <= 10:
            return bracket[rank - 1]
            
    elif "duoshowdown" in m:
        bracket = find_bracket(trophies, DUO_SHOWDOWN)
        if bracket and rank and 1 <= rank <= 5:
            return bracket[rank - 1]
            
    elif "trio" in m:
        bracket = find_bracket(trophies, TRIO_MODES)
        if bracket and rank and 1 <= rank <= 4:
            return bracket[rank - 1]
            
    elif "duels" in m:
        bracket = find_bracket(trophies, DUELS)
        if bracket and result:
            return bracket[0] if result == "victory" else bracket[1]
            
    elif "arena" in m:
        bracket = find_bracket(trophies, BRAWL_ARENA)
        if bracket and result:
            return bracket[0] if result == "victory" else bracket[1]
            
    else:
        # Standard 3v3 / 5v5
        bracket = find_bracket(trophies, TEAM_MODES)
        if bracket and result:
            return bracket[0] if result == "victory" else bracket[1]
            
    return 0
