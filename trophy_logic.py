def find_bracket(trophies, table):
    """Finds the correct trophy/rank bracket based on current trophies."""
    for bracket in sorted(table.keys(), key=lambda x: x[0], reverse=True):
        if trophies >= bracket[0]:
            return table[bracket]
    return None

